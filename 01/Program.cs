using System;

//Siempre tiene que haber un class publico
public class Program
{
    //Igual que siempre un Main dentro
    public static void Main(string[] args)
    {
        // .Write, misma linea, .WriteLine, Separa lineas.
        string saludo;
        saludo = "Hola!!";
        int saldo = 400;

        Console.Write("Mi saldo: ");
        Console.WriteLine(saldo);
        Console.WriteLine(saludo);
        saludo = "Bueno no, adios";
        Console.WriteLine(saludo);
    }
}

